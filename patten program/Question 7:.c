#include <stdio.h>

int main()
{
int r,c,i,j,num=1;//i=rows,j=column
    scanf("%d",&r);
    scanf("%d",&c);
    
    for(i=0;i<=r;i++)
    {
        for(j=1;j<=i;j++)
        {
            printf("%d",num);
            num ++;
        }
        printf("\n");
    }
    return 0;
}
