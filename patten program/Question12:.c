#include <stdio.h>

int main()
{
    int r,i,j;//r=5
    printf("ente a row:");
    scanf("%d",&r);
    
    for(i=0;i<r;i++)
    {
        for(j=1;j<=i;j++)
        {
            printf("*");
        }
        printf("\n");
    }
    for(i=0;i<=r;i++)
    {
        for(j=5;j>i;j--)
        {
            printf("*");
        }
        printf("\n");
    }
    return 0;
}

